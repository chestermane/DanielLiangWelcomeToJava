/*
 * Write a program that displays Welcome to Java five times.
 * Lesson 1.2
 */

/**
 *
 * @author Evan
 */
public class DisplayFiveMessages {

    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("Welcome to Java.");
        }
    }

}
